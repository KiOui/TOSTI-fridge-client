#!/bin/bash

# TOSTI Environment Setup Script
# This script helps you configure API credentials for the TOSTI Fridge Client

ENV_FILE="/etc/tosti-fridge.env"

# Colors
GREEN='\033[0;32m'
RED='\033[0;31m'
YELLOW='\033[1;33m'
